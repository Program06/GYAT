Inleiding

Dit privacybeleid beschrijft hoe we persoonlijke informatie verzamelen, gebruiken, openbaar maken en beschermen wanneer u onze website en diensten gebruikt als onderdeel van onze reisorganisatie. We hechten veel waarde aan uw privacy en streven ernaar om uw persoonlijke informatie te beschermen in overeenstemming met de toepasselijke wetgeving op het gebied van gegevensbescherming en privacy, waaronder de Algemene Verordening Gegevensbescherming (AVG) in Europa.

Door gebruik te maken van onze website en diensten, gaat u akkoord met de verzameling en het gebruik van uw persoonlijke informatie zoals beschreven in dit privacybeleid. We kunnen dit beleid van tijd tot tijd bijwerken. Raadpleeg regelmatig deze pagina om op de hoogte te blijven van eventuele wijzigingen.

Verzameling van persoonlijke informatie

Wanneer u gebruikmaakt van onze website en diensten, kunnen we bepaalde persoonlijke informatie van u verzamelen. Dit kan onder meer uw naam, e-mailadres, telefoonnummer, postadres, geboortedatum, paspoortgegevens, betaalgegevens, voorkeuren en andere gegevens zijn die u vrijwillig verstrekt wanneer u zich registreert voor een account, een reis boekt, nieuwsbrieven ontvangt, deelneemt aan enquêtes of anderszins met ons communiceert.

We kunnen ook automatisch bepaalde informatie verzamelen wanneer u onze website en diensten gebruikt, zoals uw IP-adres, browsertype, besturingssysteem, verwijzende URL's, informatie over uw activiteiten op onze website en andere soortgelijke gegevens. Deze informatie kan worden verzameld met behulp van verschillende technologieën, zoals cookies, pixels, logbestanden en webbakens.

Gebruik van persoonlijke informatie

We gebruiken de verzamelde persoonlijke informatie om de diensten te leveren die u hebt aangevraagd, zoals het boeken van reizen, het verstrekken van reisgerelateerde informatie en het beheren van uw account. Daarnaast kunnen we uw persoonlijke informatie gebruiken voor de volgende doeleinden:

Communicatie: We kunnen uw persoonlijke informatie gebruiken om met u te communiceren over uw boekingen, reisupdates, nieuwsbrieven en andere relevante informatie met betrekking tot onze diensten. Deze communicatie kan per e-mail, telefoon of post plaatsvinden.

Klantenservice: We kunnen uw persoonlijke informatie gebruiken om uw vragen te beantwoorden, klachten te behandelen en u te voorzien van de nodige ondersteuning bij uw reiservaring. We streven ernaar om u een uitstekende klantenservice te bieden.

Marketing: Met uw toestemming kunnen we uw persoonlijke informatie gebruiken om u gerichte marketingcommunicatie te sturen over onze producten, diensten, promoties en aanbiedingen die mogelijk interessant voor u zijn. Dit kan via verschillende kanalen gebeuren, zoals e-mail, advertenties op sociale media

Verbetering van onze diensten: We kunnen uw persoonlijke informatie gebruiken om inzicht te krijgen in het gebruik van onze website en diensten, en om onze diensten en functionaliteiten te verbeteren. Door het analyseren van gegevens, zoals gebruikersgedrag en voorkeuren, kunnen we trends identificeren en aanpassingen aanbrengen om uw gebruikerservaring te verbeteren.

Veiligheid en fraudepreventie: We kunnen uw persoonlijke informatie gebruiken om de veiligheid van onze website, diensten en transacties te waarborgen. Dit omvat het detecteren en voorkomen van frauduleuze activiteiten, het beschermen tegen ongeautoriseerde toegang tot uw account en het handhaven van de algemene veiligheid van onze gebruikers.

Juridische naleving: In sommige gevallen kunnen we verplicht zijn om uw persoonlijke informatie te verzamelen en te gebruiken om te voldoen aan wettelijke verplichtingen, zoals het voldoen aan verzoeken van wetshandhavingsinstanties, het naleven van gerechtelijke procedures of het voldoen aan andere wettelijke vereisten.

Openbaarmaking van persoonlijke informatie

We kunnen uw persoonlijke informatie delen met derden in overeenstemming met dit privacybeleid en om de hierboven beschreven doeleinden te vervullen. Deze derden kunnen omvatten:

Dienstverleners: We kunnen uw persoonlijke informatie delen met externe dienstverleners die namens ons bepaalde taken uitvoeren, zoals betalingsverwerking, klantenservice, marketingactiviteiten, gegevensanalyse en IT-ondersteuning. Deze dienstverleners hebben alleen toegang tot de persoonlijke informatie die nodig is om hun taken uit te voeren en zijn contractueel verplicht om de vertrouwelijkheid en veiligheid van uw gegevens te waarborgen.

Zakenpartners: We kunnen uw persoonlijke informatie delen met zakenpartners waarmee we samenwerken om reisdiensten aan te bieden. Dit kan bijvoorbeeld een luchtvaartmaatschappij, hotelketen of autoverhuurbedrijf zijn. Het delen van uw informatie met deze partners is noodzakelijk om uw reis te boeken en uit te voeren.

Wettelijke vereisten: We kunnen uw persoonlijke informatie openbaar maken wanneer dit wettelijk vereist is, bijvoorbeeld om te voldoen aan een gerechtelijk bevel, een juridische procedure of een verzoek van wetshandhavingsinstanties.

Geanonimiseerde gegevens: We kunnen geaggregeerde en geanonimiseerde gegevens delen met derden voor statistische analyse, marktonderzoek, demografische informatie of andere doeleinden. Deze gegevens bevatten geen persoonlijk identificeerbare informatie.

Gegevensbeveiliging

We nemen passende technische en organisatorische maatregelen om uw persoonlijke informatie te beveiligen en te beschermen tegen ongeautoriseerde toegang, verlies, misbruik of openbaarmaking. We gebruiken bijvoorbeeld beveiligde verbindingen (SSL/TLS) voor gegevensoverdracht
